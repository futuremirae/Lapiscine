#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	arr1[2];
	char	arr2[2];

	a = 0;
	b = 0;
	while (a < 99)
	{
		arr1[0] = a / 10 + '0';//
		arr1[1] = a % 10 + '0';
		b = a + 1;
		b = 0;
		while (b <= 99)
		{
			arr2[0] = b / 10 + '0';
			arr2[1] = b % 10 + '0';
			write(1, arr1, 2);
			write(1, " ", 1);
			write(1, arr2, 2);
			if (a != 98)
				write (1, ", ", 2);
			b++;
		}
	}
	a++;
}
